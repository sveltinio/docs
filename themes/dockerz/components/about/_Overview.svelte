<h2
	class="text-lg font-normal text-skin-heading hover:text-skin-heading-hover dark:text-skin-heading-dark dark:hover:text-skin-heading-dark-hover lg:text-2xl"
>
	What is Sveltin?
</h2>
<p
	class="text-base font-light text-skin-body dark:text-skin-body-dark sm:text-lg md:mt-5 md:text-xl"
>
	Sveltin is a CLI (Command Line Interface) created to <span class="mark"
		>boost the developers productivity</span
	>
	working on SvelteKit powered static websites.
</p>

<p
	class="text-base font-light text-skin-body dark:text-skin-body-dark sm:text-lg md:mt-5 md:text-xl"
>
	A small set of commands, subcommands and flags will help you generating typical stuff and let
	you focused on the content (and design).
	<span class="mark"
		>Sveltin gives you a complete toolbox to be quickly up & running, without locking your
		creativity.</span
	> There is nothing new to learn that cannot be done in 5 minutes to get started with Sveltin.
</p>
<p
	class="text-base font-light text-skin-body dark:text-skin-body-dark sm:text-lg md:mt-5 md:text-xl"
>
	Inspired by Hugo and its approach, Sveltin brings some of the conventions of Hugo combining them
	to the power and beauty of Svelte and Svelte-Kit.
</p>

<p
	class="text-base font-light text-skin-body dark:text-skin-body-dark sm:text-lg md:mt-5 md:text-xl"
>
	A Sveltin-based project stands on the shoulder of the standard <span class="font-mono text-base"
		>svelte-kit</span
	>
	starter by adding a
	<span class="mark">
		convention over configuration approach, auto generated code for common use patterns, ready
		to use components and much more.</span
	>
</p>
